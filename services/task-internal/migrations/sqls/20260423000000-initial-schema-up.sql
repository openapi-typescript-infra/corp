CREATE TABLE task_events (
  task_event_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  task_id bigint NOT NULL REFERENCES tasks (task_id),
  task_step_id bigint REFERENCES task_steps (task_step_id),
  event_type text NOT NULL,
  actor_type entity_type_enum,
  actor_uuid uuid,
  from_status task_status_enum,
  to_status task_status_enum,
  message text,
  extra_data jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_task_events_task ON task_events (task_id, created_at);

CREATE INDEX idx_task_events_step ON task_events (task_step_id, created_at);

CREATE INDEX idx_task_events_type ON task_events (event_type, created_at);

