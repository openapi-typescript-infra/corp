DO $do$
BEGIN IF EXISTS (
        SELECT
        FROM pg_catalog.pg_roles
        WHERE rolname = 'task-manager'
    ) THEN RAISE NOTICE 'Role "task-manager" already exists. Skipping.';

ELSE CREATE ROLE "task-manager";

END IF;

END $do$;

CREATE OR REPLACE FUNCTION set_updated_at() RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

GRANT USAGE ON ALL SEQUENCES IN SCHEMA public TO "task-manager";

CREATE TABLE task_types (
  task_type_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL UNIQUE,
  description text,
  default_priority smallint,
  created_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at timestamptz(3)
);

GRANT
SELECT
,
  INSERT,
UPDATE ON TABLE task_types TO "task-manager";

CREATE TRIGGER task_types BEFORE
UPDATE ON task_types FOR EACH ROW
EXECUTE PROCEDURE set_updated_at ();

CREATE TYPE task_status_enum AS ENUM('todo', 'in-progress', 'waiting', 'blocked', 'done', 'skipped');

CREATE TYPE entity_type_enum AS ENUM('user', 'agent', 'service', 'system');

CREATE TABLE tasks (
  task_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  task_uuid uuid NOT NULL DEFAULT gen_random_uuid(),
  task_type_id bigint NOT NULL REFERENCES task_types (task_type_id),
  idempotency_key text,
  created_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at timestamptz(3)
);

GRANT
SELECT
,
  INSERT,
UPDATE ON TABLE tasks TO "task-manager";

-- Indexes on tasks
CREATE UNIQUE INDEX idx_tasks_by_uuid ON tasks (task_uuid);

CREATE INDEX idx_tasks_by_type ON tasks (task_type_id, created_at);

CREATE INDEX idx_tasks_by_created_at ON tasks (created_at, task_id)
WHERE deleted_at IS NULL;

CREATE UNIQUE INDEX idx_tasks_idempotency_key ON tasks (task_type_id, idempotency_key)
WHERE
  idempotency_key IS NOT NULL
  AND deleted_at IS NULL;

CREATE INDEX idx_tasks_by_idempotency_key ON tasks (idempotency_key)
WHERE
  idempotency_key IS NOT NULL
  AND deleted_at IS NULL;

CREATE TRIGGER tasks BEFORE
UPDATE ON tasks FOR EACH ROW
EXECUTE PROCEDURE set_updated_at ();

