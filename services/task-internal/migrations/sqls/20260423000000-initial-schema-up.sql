CREATE TABLE task_steps (
  task_step_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  task_step_uuid uuid NOT NULL DEFAULT gen_random_uuid(),
  task_id bigint NOT NULL REFERENCES tasks (task_id),
  step_type_id bigint NOT NULL REFERENCES step_types (step_type_id),
  depends_on_step_id bigint REFERENCES task_steps (task_step_id),
  status task_status_enum NOT NULL DEFAULT 'todo',
  due_at timestamptz(3),
  started_at timestamptz(3),
  completed_at timestamptz(3),
  extra_data jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at timestamptz(3)
);

