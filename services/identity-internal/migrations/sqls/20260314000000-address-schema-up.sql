CREATE EXTENSION IF NOT EXISTS postgis;

CREATE TABLE
  addresses (
    address_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    -- SHA1 hash of normalized address components for deduplication
    address_key TEXT NOT NULL,
    line_1 TEXT,
    line_2 TEXT,
    city TEXT NOT NULL,
    state TEXT NOT NULL,
    postal_code TEXT NOT NULL,
    country CHAR(2) NOT NULL,
    geolocation GEOGRAPHY (POINT, 4326),
    detail jsonb,
    superseded_by_address_id BIGINT REFERENCES addresses (address_id),
    created_at TIMESTAMP DEFAULT timezone ('utc'::TEXT, NOW()) NOT NULL,
    updated_at TIMESTAMP
  );

CREATE INDEX idx_geography_location ON addresses USING GIST (geolocation);

CREATE UNIQUE INDEX idx_addresses_by_key ON addresses (address_key);

CREATE TABLE
  address_scopes (
    address_scope_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    address_scope_name TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT timezone ('utc'::TEXT, NOW()) NOT NULL,
    updated_at TIMESTAMP
  );

CREATE TABLE
  address_map (
    address_map_uuid UUID DEFAULT gen_random_uuid () NOT NULL PRIMARY KEY,
    address_id BIGINT NOT NULL REFERENCES addresses (address_id),
    address_scope_id INT NOT NULL REFERENCES address_scopes (address_scope_id),
    created_at TIMESTAMP DEFAULT timezone ('utc'::TEXT, NOW()) NOT NULL,
    updated_at TIMESTAMP
  );

CREATE UNIQUE INDEX idx_address_map_by_address_id ON address_map (address_id, address_scope_id);

GRANT SELECT ON address_scopes TO "identity-manager";

GRANT INSERT, UPDATE, SELECT ON addresses TO "identity-manager";

GRANT INSERT, UPDATE, SELECT ON address_map TO "identity-manager";

