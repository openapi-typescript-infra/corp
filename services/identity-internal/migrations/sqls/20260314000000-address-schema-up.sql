CREATE FUNCTION create_address (
  _address_key TEXT,
  _address_scope_name TEXT,
  _line_1 TEXT,
  _line_2 TEXT,
  _city TEXT,
  _state TEXT,
  _postal_code TEXT,
  _country CHARACTER,
  _geolocation GEOGRAPHY (POINT, 4326) = NULL,
  _detail jsonb = NULL
) RETURNS UUID LANGUAGE plpgsql AS $$
  DECLARE
    _address_id bigint;
    _address_scope_id int;
    _address_map_uuid uuid;
  BEGIN
    SELECT address_scope_id INTO _address_scope_id
    FROM address_scopes
    WHERE address_scope_name = _address_scope_name;

    IF _address_scope_id IS NULL THEN
      RAISE EXCEPTION 'Address scope % does not exist', _address_scope_name;
    END IF;

    SELECT COALESCE(superseded_by_address_id, address_id) INTO _address_id
    FROM addresses
    WHERE address_key = _address_key;

    IF _address_id IS NULL THEN
      INSERT INTO addresses
        (address_key, line_1, line_2, city, state, postal_code, country, geolocation, detail)
      VALUES
        (_address_key, _line_1, _line_2, _city, _state, _postal_code, _country, _geolocation, _detail)
      ON CONFLICT (address_key) DO UPDATE SET
        geolocation = COALESCE(EXCLUDED.geolocation, addresses.geolocation),
        detail = COALESCE(EXCLUDED.detail, addresses.detail)
      RETURNING address_id INTO _address_id;
    END IF;

    INSERT INTO address_map
      (address_id, address_scope_id)
    VALUES
      (_address_id, _address_scope_id)
    ON CONFLICT
      (address_id, address_scope_id)
    DO UPDATE SET address_id = EXCLUDED.address_id
    RETURNING address_map_uuid INTO _address_map_uuid;

    RETURN _address_map_uuid;
  END;
  $$;
